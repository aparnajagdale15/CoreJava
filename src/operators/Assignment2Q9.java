package operators;

public class Assignment2Q9 {

	public static void main(String[] args) {
		int x =5;
		System.out.println(x++);
		System.out.println(x);
		System.out.println(++x);
		System.out.println(x);
		
		System.out.println(x--);
		System.out.println(x);
		System.out.println(--x);
		System.out.println(x);
	}

}
